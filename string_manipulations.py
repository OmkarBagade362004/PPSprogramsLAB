s = input("Enter string: ")

#length of string
print(len(s))
#count of the substring in the string
print(s.count('o'))
#capitalise the string
print(s.capitalize())
#uppercase the string
print(s.upper())
#lowercase the string
print(s.lower())
#title 
print(s.title())

print(s.isalpha())
print(s.isalnum())
print(s.isdigit())
print(s.isspace())
